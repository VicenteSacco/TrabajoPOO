    private void ButtonActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonActualizarActionPerformed
        // TODO add your handling code here:
        int actualizarfecha = Tabla.getSelectedRow();
        
        String cadena;
        SimpleDateFormat formato = new SimpleDateFormat("dd/MM/yyyy");
        cadena = JOptionPane.showInputDialog("Ingrese nueva fecha: ");
            
            Date fecha = null;
            
            try {
                fecha = formato.parse(cadena);
            } 
            catch (ParseException ex) {
                System.out.println(ex);
            }
            
            Fecha f = new Fecha();
            f.setFecha(fecha);
            
            Principal.ListaFecha.set(actualizarfecha, f);
            JOptionPane.showMessageDialog(null, "Fecha actualizada con Ã©xito.");
            llenarTabla();
        
    }//GEN-LAST:event_ButtonActualizarActionPerformed

